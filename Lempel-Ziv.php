require __DIR__.'/vendor/autoload.php';

$cli = new CLImate();

$input = $cli->input('Nhập chuỗi cần mã hóa:');
$source = $input->prompt();
$k = strlen(count_chars($source, 3));

$prefixes = [];
for ($i = 0; $i < strlen($source); ++$i) {
    $len = 1;
    while (true) {
        $needle = substr($source, $i, $len);
        if (!in_array($needle, $prefixes, true)) {
            $prefixes[] = $needle;
            break;
        }
        ++$len;
    }
    $i += $len - 1;
}

$rs = [];
foreach ($prefixes as $prefix) {
    $r = 0;
    if (strlen($prefix) > 1) {
        $r = array_search(substr($prefix, 0, -1), $prefixes, true) + 1;
    }
    $rs[] = [$r, substr($prefix, -1, 1)];
}

$d = [];
foreach ($rs as $item) {
    /* @var int[] $item */
    $d[] = $item[0] * $k + $item[1];
}

$table = [];
foreach ($d as $i => $data) {
    $len = (int) log(($i + 1) * $k, 2) + 1;
    $encode = decbin($data);
    $table[] = [
        'I' => $prefixes[$i],
        '(r, s)' => sprintf('(%d, %d)', $rs[$i][0], $rs[$i][1]),
        'D' => $data,
        'L' => $len,
        'Mã hóa' => str_repeat('0', $len - strlen($encode)).$encode,
    ];
}

$cli->table($table);
